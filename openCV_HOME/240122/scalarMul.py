import numpy as np 

a = np.array([[1,2,3],[4,5,6]])
print(a)

b = np.array([[7,8,9],[10,11,12]])
print(b)

#스칼라곱 (스칼라곱은 실수 * 행렬 )

print(-1*a)
print(7.81*a)
print(0*a)
print((1/2)*a)
print(np.pi*a)
print(np.e*a)
print(np.sqrt(2)*a)
print(np.sqrt(a))

